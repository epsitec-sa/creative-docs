namespace Epsitec.Cresus.DataLayer.Expressions
{


	/// <summary>
	/// The <c>UnaryComparator</c> enumeration defines the possible unary comparison that can be
	/// made to test a value.
	/// </summary>
	public enum UnaryComparator
	{


		/// <summary>
		/// The comparison that checks that the value is null.
		/// </summary>
		IsNull,


		/// <summary>
		///  The comparison that checks that the value is not null.
		/// </summary>
		IsNotNull,

		/// <summary>
		///  The comparison that checks that the value is even
		/// </summary>
		IsEven,

		/// <summary>
		///  The comparison that checks that the value is odd.
		/// </summary>
		IsOdd,


	}


}
