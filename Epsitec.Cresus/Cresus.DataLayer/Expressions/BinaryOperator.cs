namespace Epsitec.Cresus.DataLayer.Expressions
{


	/// <summary>
	/// The <c>BinaryOperator</c> enumeration defines the possible operations that can be used to
	/// combine two <see cref="DataExpression"/> together.
	/// </summary>
	public enum BinaryOperator
	{


		/// <summary>
		/// The logical and operation.
		/// </summary>
		And,


		/// <summary>
		/// The logical or operation.
		/// </summary>
		Or,


	}


}
