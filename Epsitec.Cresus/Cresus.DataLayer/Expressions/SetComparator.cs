namespace Epsitec.Cresus.DataLayer.Expressions
{


	/// <summary>
	/// The <c>SetComparator</c> enumeration defines the possible comparison that can be made
	/// between a value and a set.
	/// </summary>
	public enum SetComparator
	{


		/// <summary>
		/// The operator to check if a value is included in a set.
		/// </summary>
		In,


		/// <summary>
		/// The operator to check if a value is not included in a set.
		/// </summary>
		NotIn,


	}


}
