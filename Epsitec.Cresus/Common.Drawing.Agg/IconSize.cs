namespace Epsitec.Common.Drawing
{
	/// <summary>
	/// The <c>IconSize</c> enumeration defines standard icon sizes.
	/// </summary>
	public enum IconSize
	{
		/// <summary>
		/// Default icon size, based on the icon type.
		/// </summary>
		Default,

		/// <summary>
		/// Small icon size, about the same size as the text.
		/// </summary>
		Small,
		
		/// <summary>
		/// Normal icon size, about the size of a file icon.
		/// </summary>
		Normal,
	}
}
