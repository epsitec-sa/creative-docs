namespace Epsitec.Common.Widgets
{
	/// <summary>
	/// The <c>TabNavigationDir</c> enumeration defines the direction of the
	/// Tab navigation.
	/// </summary>
	public enum TabNavigationDir
	{
		/// <summary>
		/// No direction specified.
		/// </summary>
		None=0,

		/// <summary>
		/// Move forwards.
		/// </summary>
		Forwards=1,

		/// <summary>
		/// Move backwards.
		/// </summary>
		Backwards=-1
	}
}
