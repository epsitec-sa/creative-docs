namespace Epsitec.Cresus.Database
{
	/// <summary>
	/// The <c>SqlSortOrder</c> enumeration defines the sort order.
	/// </summary>
	public enum SqlSortOrder			
	{
		/// <summary>
		/// No sort order.
		/// </summary>
		None,

		/// <summary>
		/// ASCENDING sort order.
		/// </summary>
		Ascending,

		/// <summary>
		/// DESCENDING sort order.
		/// </summary>
		Descending,
	}
}
