namespace Epsitec.Common.OpenType
{
    /// <summary>
    /// The <c>FontWeight</c> enumeration describes the weight of
    /// a font (normal, bold, thin, etc.)
    /// </summary>
    public enum FontWeight
    {
        Normal = 400, //	standard weight

        Thin = 100,
        ExtraLight = 200,
        Light = 300,
        Medium = 500,
        SemiBold = 600,
        Bold = 700,
        ExtraBold = 800,
        Black = 900
    }
}
